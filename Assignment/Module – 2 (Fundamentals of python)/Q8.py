# Q8.  Write a Python program to test whether a passed letter is a vowel or not.

n=input("Enter Any Letter Between (A to Z / a to z): ")

if n == "A" or n == "E" or n == "I" or n == "o" or n == "u" or n == "a" or n == "e" or n == "i" or n == "o" or n == "u":
    print("This Letter is Vowel...")
else:
    print("This Letter is Not Vowel...")
